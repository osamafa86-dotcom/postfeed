<span class="sp-eyebrow">السياسة التحريرية</span>

<p>نُنظّم عملنا التحريري وفق مبادئ واضحة تُعلَن للقارئ ونُحاسَب عليها. هذه السياسة تصف كيف نختار الأخبار، كيف نتحقّق منها، وكيف نتعامل مع الحالات الحسّاسة.</p>

<h2>١. الاستقلالية التحريرية</h2>
<p>القرارات التحريرية في نيوز فيد مستقلّة عن التمويل والإعلانات. لا يجوز لأيّ مُعلن أو شريك أو جهة خارجية التأثير على اختيار الأخبار، أو ترتيبها، أو حجب خبر من الظهور. هيئة التحرير وحدها تتحمّل مسؤولية كلّ ما يُنشر.</p>

<h2>٢. اختيار المصادر</h2>
<p>لا نُضيف مصدرًا إلى النظام قبل أن يستوفي الشروط التالية:</p>
<ul>
  <li><strong>الشفافية</strong> — هويّة المؤسّسة وفريق التحرير معروفة ومعلن عنها.</li>
  <li><strong>المصداقية</strong> — سجلّ تحريري لا تقلّ مدّته عن سنة، وسُمعة مهنيّة تُثبّت صحّة معظم ما يُنشر.</li>
  <li><strong>الإفصاح</strong> — سياسة تصحيح أخطاء مُعلنة، وعنوان اتصال حقيقي.</li>
  <li><strong>عدم الاستعراض</strong> — نستبعد المواقع التي تعتمد على عناوين مضلّلة (clickbait) بشكل منهجي.</li>
</ul>
<p>يُراجع فريق التحرير قائمة المصادر كلّ ربع سنة، ويزيل المصادر التي تخلّفت عن هذه المعايير.</p>

<h2>٣. اختيار الأخبار</h2>
<p>نُرتّب الأخبار تلقائيًا بناءً على:</p>
<ul>
  <li>تاريخ النشر (الأحدث أولًا في الأقسام الزمنيّة).</li>
  <li>مدى تغطية الحدث عبر عدّة مصادر مستقلّة (كلما غطّى الحدث أكثر من مصدر، ازدادت أهمّيته).</li>
  <li>تفاعل القرّاء (في قسم الأكثر قراءةً فقط، وبلا تأثير على بقية الأقسام).</li>
</ul>
<p>لا يتدخّل محرّر في ترتيب الأخبار الآلية إلا في حالتين: تصحيح خطأ تقني أو إبراز خبر عاجل ذي أهمية قصوى، ويُوسَم حينها بعلامة "عاجل".</p>

<h2>٤. الفصل بين الخبر والرأي</h2>
<p>كلّ ما ينشر على نيوز فيد هو إمّا <strong>خبر</strong> أو <strong>ملخّص لخبر</strong>. لا ننشر مقالات رأي باسم المنصّة، ولا نعدّل عناوين المصادر أو نصوصها بما يُغيّر المعنى. في حال اضطُررنا لاختصار عنوان طويل نضع إشارة واضحة أنّ النصّ مُحرَّر.</p>

<h2>٥. الذكاء الاصطناعي والتلخيص</h2>
<p>نستخدم نماذج لغة لتوليد ملخّصات موجزة لبعض الأخبار الطويلة. نلتزم بالضوابط التالية:</p>
<ul>
  <li>الملخّص مُوسَم دائمًا بعبارة <em>"ملخّص ذكي"</em>، لا يلتبس بالنصّ الأصلي.</li>
  <li>لا يُضيف الذكاء الاصطناعي معلومات غير موجودة في الخبر الأصلي.</li>
  <li>أيّ ملخّص يتبيّن أنّه غير دقيق يُزال فورًا عند إخطارنا.</li>
  <li>الرابط الأصلي للخبر يبقى بارزًا ليتمكّن القارئ من الرجوع إليه في أيّ وقت.</li>
</ul>

<h2>٦. التحقّق من الحقائق</h2>
<p>نُعطي الأولوية للأخبار التي تُغطّيها مصادر متعددة مستقلّة. عندما يظهر خبر في مصدر واحد فقط ويتضمّن ادّعاءً خطيرًا (اغتيال، عدد ضحايا، اتّهام شخصي…)، نُضيف علامة <strong>"مصدر واحد"</strong> ونُوضح للقارئ ضرورة التحفّظ حتى تؤكّد مصادر أخرى. نعتمد في التحقّق على مراجع مهنيّة مثل وكالات الأنباء الدولية والمكاتب الحكومية الرسمية.</p>

<h2>٧. المحتوى الحسّاس</h2>
<p>في تغطية الأحداث التي تشمل ضحايا أو مشاهد صادمة:</p>
<ul>
  <li>لا نعرض صورًا صادمة دون تحذير بصري واضح.</li>
  <li>لا نذكر أسماء القاصرين ضحايا الجرائم.</li>
  <li>نتجنّب التفاصيل الطبية/النفسية التي قد تُسبّب ضررًا للعائلات.</li>
  <li>لا نستخدم لغة تحريض أو تصنيف جماعي.</li>
</ul>

<h2>٨. المصادر الحكومية والحزبية</h2>
<p>نُدرج مصادر ذات توجّه حكومي أو حزبي معروف لتحقيق التوازن في التغطية، لكن نُوضح هويّتها. لا يُقدَّم مصدر حكومي على أنّه مصدر مستقلّ، ولا يُمنح مصدر حزبي موقعًا أولى في الصفحة بسبب انتمائه.</p>

<h2>٩. حقوق المصدر الأصلي</h2>
<p>نحترم حقوق النشر لكلّ مصدر:</p>
<ul>
  <li>نعرض العنوان والملخّص القصير فقط، ولا ننسخ المقال كاملًا.</li>
  <li>نُبقي رابط المصدر الأصلي بارزًا على كلّ بطاقة خبر.</li>
  <li>إذا طلب مصدر حذف أرشيفه من نيوز فيد، نفعل ذلك خلال ٤٨ ساعة.</li>
</ul>

<h2>١٠. الإعلانات</h2>
<p>أيّ محتوى إعلاني يُعرَض على الموقع يُميَّز بوضوح بكلمة <strong>"إعلان"</strong> ولا يُخلط مع المحتوى التحريري. لا نقبل إعلانات مضلّلة، أو تتعلّق بمنتجات محظورة، أو تستخدم صورًا صادمة.</p>

