<h2>٤. الأطراف الثالثة</h2>
<p>نستخدم خدمات خارجية لمساعدتنا في تشغيل الموقع والتطبيق:</p>
<ul>
  <li><strong>Google Fonts</strong> — لعرض الخطوط العربية.</li>
  <li><strong>Google Search Console / Bing Webmaster</strong> — أدوات مطوّرين لتحليل ظهورنا في محرّكات البحث.</li>
  <li><strong>Firebase Cloud Messaging (Google)</strong> — لإرسال الإشعارات الفورية لتطبيق الجوّال. يستلم رمز الجهاز فقط دون أي بيانات شخصيّة. <a href="https://firebase.google.com/support/privacy" rel="noopener" target="_blank">سياسة Firebase</a>.</li>
  <li><strong>Sign in with Apple</strong> — لتسجيل الدخول الآمن لمستخدمي iOS. <a href="https://www.apple.com/legal/privacy/" rel="noopener" target="_blank">سياسة Apple</a>.</li>
  <li><strong>Google Sign-In</strong> — لتسجيل الدخول عبر حساب Google (اختياري). <a href="https://policies.google.com/privacy" rel="noopener" target="_blank">سياسة Google</a>.</li>
  <li><strong>OpenStreetMap</strong> — لعرض الخريطة في ميزة "أخبار حسب الموقع". لا تتلقى أي بيانات شخصيّة.</li>
  <li><strong>مزوّد الاستضافة</strong> — يخزّن بيانات الموقع وسجلّات الخوادم.</li>
</ul>
